package co.za.twoid.model;

public class Type10Model {
	
	private String IDC;
	private String IMT;
	private String HLL;
	private String VLL;
	private String CGA;
	private String CSP;
	private byte[] DAT;
	
	public String getIDC(){
		return IDC;
	}
	public void setIDC(String IDC){
		this.IDC = IDC;
	}
	public String getIMT(){
		return IMT;
	}
	public void setIMT(String IMT){
		this.IMT = IMT;
	}
	public String getHLL(){
		return HLL;
	}
	public void setHLL(String HLL){
		this.HLL = HLL;
	}
	public String getVLL(){
		return VLL;
	}
	public void setVLL(String VLL){
		this.VLL = VLL;
	}
	public String getCGA(){
		return CGA;
	}
	public void setCGA(String CGA){
		this.CGA = CGA;
	}
	public String getCSP(){
		return CSP;
	}
	public void setCSP(String CSP){
		this.CSP = CSP;
	}
	public byte[] getDAT(){
		return DAT;
	}
	public void setDAT(byte[] DAT){
		this.DAT = DAT;
	}
}
